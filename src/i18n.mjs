export const translations = Object.freeze(
  Object.fromEntries(
    SUPPORTED_LANGUAGES.map((language) => [
      language,
      Object.freeze({
        ...en,
        ...(overrides[language] ?? {})
      })
    ])
  )
);

export function detectLanguage(languageTags = []) {
  for (const tag of languageTags) {
    const normalized = normalizeLanguage(tag);

    if (normalized) {
      return normalized;
    }
  }

  return DEFAULT_LANGUAGE;
}

export function normalizeLanguage(languageTag) {
  if (!languageTag || typeof languageTag !== 'string') {
    return null;
  }

  const normalized = languageTag.trim().replace(/_/g, '-');
  const lower = normalized.toLowerCase();

  if (lower.startsWith('zh')) {
    if (lower.includes('hant') || lower.includes('tw') || lower.includes('hk') || lower.includes('mo')) {
      return 'zh-Hant';
    }

    return 'zh-Hans';
  }

  const base = lower.split('-')[0];
  return isSupportedLanguage(base) ? base : null;
}

export function createTranslator(language) {
  const activeLanguage = isSupportedLanguage(language) ? language : DEFAULT_LANGUAGE;
  const dictionary = translations[activeLanguage];

  return (key, values = {}) => {
    const template = dictionary[key] ?? translations[DEFAULT_LANGUAGE][key] ?? key;
    return template.replace(/\{(\w+)\}/g, (_, name) => String(values[name] ?? `{${name}}`));
  };
}

export function isSupportedLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language);
}
